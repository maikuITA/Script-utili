# funzione per fancy print
def fancy_print(s):
    sos = ""
    for i in range(len(s)):
        sos += str(s[i]) + " "
    print(sos)

################################################################################################################################
# PROGRAMMA
################################################################################################################################

coppie = []

# inverto le coppie
for i in range(len(coppie)):
    coppie[i] = coppie[i][::-1] # inverto le coppie

# output
print("Coppie invertite a specchio:")
fancy_print(coppie)  